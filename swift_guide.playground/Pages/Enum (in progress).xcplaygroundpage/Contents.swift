// =================================================================================

// Enum - ещё один тип данных. Он предназначен для создания и ранения простых констант.

enum Elements: String {
  case fire
  case earth
  case wind
  case water
  // case fire, earth, wind, water - или в одну строку
}

func translatedElement(element: Elements) {
  switch element {
  case .earth:
    print("Земля")
  case .fire:
    print("Огонь")
  case .wind:
    print("Ветер")
  case .water:
    print("Вода")
  }
}

translatedElement(element: .wind)

